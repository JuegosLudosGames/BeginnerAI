package me.trio;
/**
 * Used for making lines
 * Set an x coordinate and a y coordinate float variable
 * 
 */
public class Vector2D {
	
	/**
	 * Constructs a new Vector 2D object
	 * @param x sets x coordinate
	 * @param y sets y coordinate
	 */
	public Vector2D(float x, float y) {
		//used t
		this.x = x;
		this.y = y;
	}
	/**
	 * initial float x value to reference in Vector2D constructor
	 */
	public float x;
	/**
	 * initial float y value to reference in Vector2D constructor
	 */
	public float y;
}
